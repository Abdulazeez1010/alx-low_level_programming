#include "3-calc.h"

/**
 * op_add - Addition function
 * @a : First operand
 * @b : Second operand
 * Return: Returns sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtraction function
 * @a : First operand
 * @b : Second operand
 * Return: Returns subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication function
 * @a : First operand
 * @b : Second operand
 * Return: Returns product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division function
 * @a : First operand
 * @b : Second operand
 * Return: Returns quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulo function
 * @a : First operand
 * @b : Second operand
 * Return: Returns remainder from division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
